package id.ac.uns.vokasi.d3ti.univ;

public class Dosen {

		private String no;
		private String nip;
		private String nama;
		
		public void setNo(String no) {
			this.no=no;
		}
		
		public String getNo() {
			return no;
		}
		
		public void setNip(String nip) {
			this.nip=nip;
		}
		
		public String getNip() {
			return nip;
		}
		
		public void setNama(String nama) {
			this.nama=nama;
		}
		
		public String getNama() {
			return nama;
		}
}
